// Assignment Operators
let x = 5;

// Addition assignment
x += 1; // x = x + 1;

// Subtraction assignment
x -= 1; // x = x - 1;

// Multiplication assignment
x *= 1; // x = x * 1;

// Division assignment
x /= 1; // x = x / 1;

//Modulus assignment
x %= 1; // x = x % 1;

//Exponential assignment
x **= 1; // x = x ** 1;

console.log(x);