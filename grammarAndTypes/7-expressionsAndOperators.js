// Assignment Operators
let x = 5;

// Addition assignment
x += 1; // x = x + 1;

// Subtraction assignment
x -= 1; // x = x - 1;

// Multiplication assignment
x *= 1; // x = x * 1;

// Division assignment
x /= 1; // x = x / 1;

//Modulus assignment
x %= 1; // x = x % 1;

//Exponential assignment
x **= 1; // x = x ** 1;

console.log(x);

/************************************************* */

// Comparison operators
console.log('3' == 3); //equal operator

console.log('3' === 3); // strict equal operator

console.log('3' != 3); // not equal

console.log('3' !== 3); // strict not equal

console.log(3 > 2); // greater than

console.log(3 < 2); // less than

console.log(3 >= 2); // greater than or equal to

console.log(3 <= 2); // less than or equal to

console.log(2 || 3); // OR operator
/************************************************* */